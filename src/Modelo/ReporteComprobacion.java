package Modelo;

/**
 *
 * @author Antonio Aranda
 */
public class ReporteComprobacion {
    private String codigo,cuenta,debe,haber;

    public ReporteComprobacion(String codigo, String cuenta, String debe, String haber) {
        this.codigo = codigo;
        this.cuenta = cuenta;
        this.debe = debe;
        this.haber = haber;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getCuenta() {
        return cuenta;
    }

    public void setCuenta(String cuenta) {
        this.cuenta = cuenta;
    }

    public String getDebe() {
        return debe;
    }

    public void setDebe(String debe) {
        this.debe = debe;
    }

    public String getHaber() {
        return haber;
    }

    public void setHaber(String haber) {
        this.haber = haber;
    }
    
}
